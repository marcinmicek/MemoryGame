public class DisplayMatrix {
    public static void showSimpleMatrix() {
        System.out.println("    1   2   3   4");
        System.out.println("A   X   X   X   X");
        System.out.println("B   X   X   X   X");
    }

    public static void showAdvancedMatrix() {
        System.out.println("    1   2   3   4   5   6   7   8");
        System.out.println("A   X   X   X   X   X   X   X   X");
        System.out.println("B   X   X   X   X   X   X   X   X");
    }
}
